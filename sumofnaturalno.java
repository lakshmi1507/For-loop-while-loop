import java.util.Scanner;

public class sumofnaturalno {
    public static void main(String[] args) {
        int n, sum = 0;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the value of n");
        n = sc.nextInt();
        while (n <= sum) {
            sum = n + sum;
            System.out.println("The sum is " + sum);

        }

    }

}
